                    Add the following shortcode to your menu to be able to change languages: <pre>[my-dictionary-menu-language-switcher]</pre>
                    If you need a plugin to use shortcodes inside of menus, we strongly recommend this one: <a href="https://wordpress.org/plugins/shortcode-in-menus/" target="_blank">"Shortcode in Menus"</a>
                </li>
                <li>
                    Replace all single quotes and double quotes in your original texts with apostrophes <b>"</b> / <b>'</b> -> <b>‘’</b><br>
                    Write apostrophes in Windows with: <b>Alt+39</b><br>
                    Write apostrophes in Mac with: <b>Option + Shift + 8</b> (for opening) & <b>Option + Shift + 9</b> (for closing).
                </li>
